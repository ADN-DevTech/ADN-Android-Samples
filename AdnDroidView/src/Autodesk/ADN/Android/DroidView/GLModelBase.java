package Autodesk.ADN.Android.DroidView;

import javax.microedition.khronos.opengles.GL10;

import android.content.Context;

//////////////////////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////////////////////
public class GLModelBase 
{
	Context _context;
	
	boolean _doInit;
	
	//////////////////////////////////////////////////////////////////////////////////////
	//
	//
	//////////////////////////////////////////////////////////////////////////////////////
	public GLModelBase(Context context)
	{
		_context = context;
		
		_doInit = true;
	}
	
	//////////////////////////////////////////////////////////////////////////////////////
	//
	//
	//////////////////////////////////////////////////////////////////////////////////////
	public void Initialize(GL10 gl, Context context) 
	{
		_doInit = false;
	}
	
	//////////////////////////////////////////////////////////////////////////////////////
	//
	//
	//////////////////////////////////////////////////////////////////////////////////////
	public void draw(GL10 gl)
	{
		gl.glColor4f(190.0f/255.0f, 190.0f/255.0f, 190.0f/255.0f, 1.0f); 
		
		if(_doInit)
		{
			Initialize(gl, _context); 
		}
	}
}
